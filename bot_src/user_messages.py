def msg_sub_config_refresh(profile_name: str = "🚀 BenderVPN Auto") -> str:
    """Parameterized sub-refresh notify (P3-UX-MESSAGES-01)."""
    name = (profile_name or "🚀 BenderVPN Auto").strip()
    return (
        "🛠 <b>Мы обновили настройки VPN на сервере</b>\n\n"
        f"Открой Happ и <b>обнови подписку</b> (кнопка 🔄 у профиля «{name}»), "
        "затем включи VPN снова.\n\n"
        "Если Happ на секунду показывает «0 серверов» — не удаляй профиль: "
        "после обновления узлы появятся сами.\n\n"
        "Страну выбирать не нужно — приложение само подключит рабочий узел.\n\n"
        "Если после обновления ошибка остаётся — напиши в поддержку."
    )


MSG_SUB_CONFIG_REFRESH = msg_sub_config_refresh()
